package it.polimi.tiw.BBB.beans;

public class User {
	
	private int userId;
	private String username;
	
	//getters and setters
	
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	
	
	
}
